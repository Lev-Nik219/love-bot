# --- Инициализация бота и диспетчера ---
bot = Bot(token=BOT_TOKEN)
dp = Dispatcher()

# --- Клавиатура ---
button_text = "💖 Получить порцию любви 💖"
keyboard = ReplyKeyboardMarkup(
    keyboard=[[KeyboardButton(text=button_text)]],
    resize_keyboard=True,
    one_time_keyboard=False
)

# --- Счётчик нажатий для каждого пользователя ---
user_click_count = {}

# --- Flask-сервер для поддержания активности ---
app = Flask(__name__)

@app.route('/')
def home():
    return "Бот работает!"

@app.route('/health')
def health():
    return "OK", 200

def run_flask():
    port = int(os.environ.get("PORT", 5000))
    app.run(host='0.0.0.0', port=port)

def start_flask():
    t = Thread(target=run_flask)
    t.daemon = True
    t.start()

# --- Хэндлер команды /start ---
@dp.message(Command("start"))
async def cmd_start(message: types.Message):
    await message.answer(
        "Жизнь моя! Я дам тебе море нежности 💕\n"
        "Нажми на кнопку ниже — и я подарю тебе свою любовь\n"
        "А каждое 7-е нажатие я порадую тебя еще одним сюрпризом.",
        reply_markup=keyboard
    )

# --- Хэндлер нажатия на кнопку ---
@dp.message(lambda message: message.text == button_text)
async def send_love(message: types.Message):
    user_id = message.from_user.id
    user_click_count[user_id] = user_click_count.get(user_id, 0) + 1
    clicks = user_click_count[user_id]

    if clicks % 7 == 0:
        poem = random.choice(POEMS)
        await message.answer(poem)
    else:
        num_messages = random.randint(5, 8)
        selected_messages = random.sample(ROMANTIC_MESSAGES, num_messages)
        for text in selected_messages:
            await message.answer(text)
            await asyncio.sleep(0.3)

# --- Запуск бота ---
async def main():
    start_flask()               # запускаем Flask в фоне
    await asyncio.sleep(2)       # даём время Flask'у открыть порт
    print("🤖 Романтический бот запущен... (каждое 7-е нажатие — стихи)")
    await dp.start_polling(bot)

if __name__ == "__main__":
    asyncio.run(main())